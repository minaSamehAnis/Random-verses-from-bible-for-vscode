// Show a random Bible verse
function showRandomVerse() {
  const randomVerse = bibleVerses[Math.floor(Math.random() * bibleVerses.length)];
  vscode.window.showInformationMessage(randomVerse);
}

function activate(context) {
  // Show random verse every minute (60000ms)
  setInterval(showRandomVerse, 60000); 

  // Register command to show random verse manually
  let disposable = vscode.commands.registerCommand('extension.showRandomVerse', showRandomVerse);
  context.subscriptions.push(disposable);

  // Show a random verse immediately after startup
  showRandomVerse();
}

function deactivate() {}

module.exports = {
  activate,
  deactivate
};